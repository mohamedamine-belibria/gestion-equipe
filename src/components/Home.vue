<template>
  <div class="container">
    <header class="jumbotron">
      <div class="fullscreen-image">
        <img
          src="../assets/images/acc.jpg"
          class="img-thumbnail"
          alt="..."
        />
      </div>
    </header>
  </div>
</template>

<script>
import UserService from "../services/user.service";

export default {
  name: "Home",
  data() {
    return {
      content: "",
    };
  },
  mounted() {
    UserService.getPublicContent().then(
      (response) => {
        this.content = response.data;
      },
      (error) => {
        this.content =
          (error.response &&
            error.response.data &&
            error.response.data.message) ||
          error.message ||
          error.toString();
      }
    );
  },
};
</script>
